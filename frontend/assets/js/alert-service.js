/**
 * AlertService â€” Value Loop v1.0
 * Persiste alertas de agentes IA en Supabase y trackea acciones del usuario.
 * 
 * Uso:
 *   await AlertService.save(alertData)          â†’ POST /agents/alerts
 *   await AlertService.action(id, 'approved')   â†’ POST /agents/alerts/{id}/action
 *   await AlertService.list('pending')          â†’ GET  /agents/alerts?status=pending
 *   await AlertService.trackOnboarding(event)   â†’ POST /onboarding/event
 */

(function (window) {
    'use strict';

    const API_BASE = (window.SAAS_CONFIG && (window.SAAS_CONFIG.apiUrl || window.SAAS_CONFIG.aiBackendUrl))
        || 'https://ecosistema-ai-v50-production.up.railway.app/api/v1';
    const SESSION_KEY = (window.SAAS_CONFIG && window.SAAS_CONFIG.sessionKey)
        || 'alpa_app_session_v1';

    function _getToken() {
        try {
            const session = JSON.parse(localStorage.getItem(SESSION_KEY) || '{}');
            return session.token || null;
        } catch { return null; }
    }

    function _headers(auth = true) {
        const h = { 'Content-Type': 'application/json' };
        if (auth) {
            const tok = _getToken();
            if (tok) h['Authorization'] = `Bearer ${tok}`;
        }
        return h;
    }

    async function _request(method, path, body, auth = true) {
        try {
            const opts = { method, headers: _headers(auth) };
            if (body) opts.body = JSON.stringify(body);
            const res = await fetch(`${API_BASE}${path}`, opts);
            if (!res.ok) {
                const err = await res.json().catch(() => ({}));
                throw new Error(err.detail || `HTTP ${res.status}`);
            }
            return await res.json();
        } catch (e) {
            console.warn(`[AlertService] ${method} ${path} failed:`, e.message);
            return null;
        }
    }

    const AlertService = {

        /**
         * Persist an alert generated by an AI agent.
         * @param {Object} alert - {agent_id, agent_name, alert_type, severity, title, message, confidence?, metadata?, cycle_id?}
         * @returns {Promise<{alert_id: string}|null>}
         */
        async save(alert) {
            const result = await _request('POST', '/agents/alerts', alert);
            if (result) {
                console.info(`[AlertService] Alert saved: ${result.alert_id} (${alert.severity})`);
            }
            return result;
        },

        /**
         * Register a user action on an alert.
         * @param {string} alertId
         * @param {'approved'|'rejected'|'snoozed'} action
         * @param {string} [comment]
         */
        async action(alertId, action, comment = null) {
            const result = await _request('POST', `/agents/alerts/${alertId}/action`, {
                action, comment
            });
            if (result) {
                console.info(`[AlertService] Alert ${alertId}: ${action}`);
            }
            return result;
        },

        /**
         * Load alerts for the current organization.
         * @param {'pending'|'approved'|'rejected'|null} status
         * @param {number} limit
         */
        async list(status = null, limit = 50) {
            const qs = status ? `?status=${status}&limit=${limit}` : `?limit=${limit}`;
            return await _request('GET', `/agents/alerts${qs}`);
        },

        /**
         * Check trial status against the backend (enforcement layer).
         * Returns {status, days_left} or null if backend unavailable.
         * @param {string} email
         */
        async checkTrial(email) {
            if (!email) return null;
            return await _request('GET', `/trials/status?email=${encodeURIComponent(email)}`, null, false);
        },

        /**
         * Track an onboarding event in Supabase.
         * @param {string} event - one of: wizard_iniciado, identidad_completada, primer_proyecto,
         *                         directorio_base, primer_scan_ia, wizard_completado, etc.
         * @param {number} [step]
         * @param {Object} [metadata]
         */
        async trackOnboarding(event, step = null, metadata = null) {
            const result = await _request('POST', '/onboarding/event', {
                event,
                step,
                metadata: metadata || {}
            });
            if (result) {
                console.info(`[AlertService] Onboarding event tracked: ${event}`);
            }
            return result;
        },

        /**
         * Submit an upgrade contact request (billing CTA).
         * Creates a lead in the CRM with origen: "upgrade_cta".
         * @param {{email, name, company, plan?, message?}} data
         */
        async submitUpgradeContact(data) {
            return await _request('POST', '/billing/contact', data, false);
        },

        /**
         * Verify trial on app start. Applies hard block if backend says expired.
         * Call this during app init, after TrialGuard.check().
         */
        async verifyTrialOnStartup() {
            try {
                const trial = JSON.parse(localStorage.getItem('agentOS_trial_v1') || '{}');
                if (!trial.email) return; // not a trial user

                const status = await this.checkTrial(trial.email);
                if (!status) return; // backend unreachable, trust localStorage

                if (status.status === 'expired' && trial.trial_active !== false) {
                    // Backend says expired, force localStorage sync
                    trial.trial_active = false;
                    trial.days_left = 0;
                    localStorage.setItem('agentOS_trial_v1', JSON.stringify(trial));
                    console.warn('[AlertService] Trial expired confirmed by backend. Updating local state.');
                    // TrialGuard will catch this on next check()
                    if (window.TrialGuard) window.TrialGuard.check();
                }
            } catch (e) {
                console.warn('[AlertService] verifyTrialOnStartup error:', e.message);
            }
        }
    };

    window.AlertService = AlertService;
    console.info('[AlertService] v1.0 â€” Value Loop ready âœ…');

})(window);
